#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int l1;
	int l2;

	l1 = ft_strlen(s1);
	l2 = ft_strlen(s2);
	ft_memccpy(s1 + l1, (char *)s2, '\0', l2 + l1 + 1);
	return (s1);
}
